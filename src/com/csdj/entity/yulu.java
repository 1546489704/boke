package com.csdj.entity;

public class yulu {
    /*
      属性--字段
     */
    private  int id;
    private  String name;
    private  String time;
    private  String yulu;
    private  String leibie;

   /*
    封装
     */

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getYulu() {
        return yulu;
    }

    public void setYulu(String yulu) {
        this.yulu = yulu;
    }

    public String getLeibie() {
        return leibie;
    }

    public void setLeibie(String leibie) {
        this.leibie = leibie;
    }

}
