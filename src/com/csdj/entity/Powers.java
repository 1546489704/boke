package com.csdj.entity;
//权限表
public class Powers {
    //自增id
    private int powerId;
    //权限名称
    private String power;

    public int getPowerId() {
        return powerId;
    }

    public void setPowerId(int powerId) {
        this.powerId = powerId;
    }

    public String getPower() {
        return power;
    }

    public void setPower(String power) {
        this.power = power;
    }
}
